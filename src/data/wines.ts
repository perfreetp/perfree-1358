export const getWineById = (id: string): Wine | undefined => {
  return wines.find(wine => wine.id === id);
};

export const getWinesByCategory = (category: string): Wine[] => {
  return wines.filter(wine => wine.category === category);
};

export const searchWines = (keyword: string): Wine[] => {
  const lowerKeyword = keyword.toLowerCase();
  return wines.filter(wine => 
    wine.name.toLowerCase().includes(lowerKeyword) ||
    wine.nameEn.toLowerCase().includes(lowerKeyword) ||
    wine.aroma.some(a => a.includes(keyword)) ||
    wine.taste.some(t => t.includes(keyword))
  );
};
